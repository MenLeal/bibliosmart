    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String autor = txt_autor.getText();
        String[] titles = {"Titulo", "Edici√≥n", "Disponibles"};
        String isbn;
        String edit;
        String disponible;
        autores = new JTable();
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(titles);
        autores.setModel(model);
        autores.setAutoResizeMode(JTable.AUTO_RESIZE_ALL_COLUMNS);
        autores.setFillsViewportHeight(true);
        jScrollPane1.setViewportView(autores);
        jScrollPane1.setHorizontalScrollBarPolicy(
                JScrollPane.HORIZONTAL_SCROLLBAR_AS_NEEDED);
        jScrollPane1.setVerticalScrollBarPolicy(
                JScrollPane.VERTICAL_SCROLLBAR_AS_NEEDED);
        try {
            Class.forName("com.mysql.cj.jdbc.Driver").newInstance();
            Connection con = DriverManager.getConnection("jdbc:mysql://127.0.0.1:3306/biblioteca", Login.usuario, Login.contra);
            PreparedStatement ps = con.prepareStatement("CALL autores('"+autor+"')");
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                isbn = rs.getString("titulo");
                edit = rs.getString("edicion");
                disponible = rs.getString("disponibles");
                model.addRow(new Object[]{isbn,edit, disponible});
                    
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed
