    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DatabaseConnectivity db = new DatabaseConnectivity(Login.usuario, Login.contra);
        String rfc = txt_rfc.getText();
        String nombre = txt_nombre.getText();
        String apellidos = txt_apellidos.getText();
        String sexo = cb_genero.getSelectedItem().toString();
        String telefono = txt_telefono.getText();
        String rfc_list = LectoresList.lector_rfc;

        String qry = "UPDATE lector SET rfc = '"+rfc+"', nombre = '" + nombre + "', apellidos = '" + apellidos + "', sexo = '" + sexo + "', telefono = '" + telefono + "' WHERE rfc = '"+rfc_list+"'";
        boolean r = db.execute(qry);
        if (r) {
            Tools.Helper.show(this, "Usuario actualizado");
            this.setVisible(false);
            new LectoresList().setVisible(true);

        } else {
            Tools.Helper.show(this, "Intentar de nuevo!");
        }


    }//GEN-LAST:event_jButton1ActionPerformed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        Tools.Helper.setCenter(this);
        String rfc_list = LectoresList.lector_rfc;
        try {
            Class.forName("com.mysql.cj.jdbc.Driver").newInstance();
            Connection con = DriverManager.getConnection("jdbc:mysql://127.0.0.1:3306/biblioteca", Login.usuario, Login.contra);
            PreparedStatement ps = con.prepareStatement("SELECT * FROM lector WHERE rfc='"+rfc_list+"'");
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                txt_rfc.setText(rs.getString("rfc"));
                txt_nombre.setText(rs.getString("nombre"));
                txt_apellidos.setText(rs.getString("apellidos"));
                txt_telefono.setText(rs.getString("telefono"));
                cb_genero.setSelectedItem(rs.getString("sexo"));
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_formWindowOpened

