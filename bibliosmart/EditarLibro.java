    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        DatabaseConnectivity db = new DatabaseConnectivity(Login.usuario,Login.contra);
        String isbn = txt_isbn.getText();
        String titulo = txt_titulo.getText();
        String edit = txt_edit.getText();
        String editorial = txt_editorial.getText();
        String precio = txt_precio.getText();
        String pag = txt_pag.getText();
        String disp = txt_disp.getText();
        String autor = txt_autor.getText();

        String sql_qry = "UPDATE libro SET ISBN = '" + isbn + "', titulo = '" + titulo + "', edicion = " + edit + " , editorial = '" + editorial + "', precio = " + precio + ", paginas = " + pag + ", disponibles="+disp+", autor='"+autor+"' WHERE ISBN = '"+isbn+"'";
        boolean r = db.execute(sql_qry);
        if (r) {
            Tools.Helper.show(this, "Libro Actualizado");
            this.setVisible(false);
            new LibrosList().setVisible(true);
        } else {
            Tools.Helper.show(this, "Intentar de Nuevo");
        }
    }//GEN-LAST:event_saveButtonActionPerformed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        Tools.Helper.setCenter(this);
        String isbn = LibrosList.book_isbn;
        try {
            Class.forName("com.mysql.cj.jdbc.Driver").newInstance();
            Connection con = DriverManager.getConnection("jdbc:mysql://127.0.0.1:3306/biblioteca",Login.usuario, Login.contra);
            PreparedStatement ps = con.prepareStatement("SELECT * FROM libro WHERE ISBN='"+isbn+"'");
            ResultSet rs = ps.executeQuery();
            if(rs.next()){
                txt_isbn.setText(rs.getString("ISBN"));
                txt_autor.setText(rs.getString("autor"));
                txt_titulo.setText(rs.getString("titulo"));
                txt_edit.setText(rs.getString("edicion"));
                txt_editorial.setText(rs.getString("editorial"));
                txt_precio.setText(rs.getString("precio"));
                txt_pag.setText(rs.getString("paginas"));
                txt_disp.setText(rs.getString("disponibles"));
            }
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_formWindowOpened

