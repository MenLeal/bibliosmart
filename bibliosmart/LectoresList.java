    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        Tools.Helper.setCenter(this);
        String[] title = {"ID", "Nombre", "Apellidos", "Genero", "Tel√©feno"};
        String rfc="";
        String nombre = "";
        String apellido ="";
        String genero = "";
        String num = "";
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(title);
        jTable1= new JTable();
        jTable1.setModel(model);
        jTable1.setAutoResizeMode(JTable.AUTO_RESIZE_ALL_COLUMNS);
        jTable1.setFillsViewportHeight(true);
        jScrollPane1.setViewportView(jTable1);
        jScrollPane1.setHorizontalScrollBarPolicy(
        JScrollPane.HORIZONTAL_SCROLLBAR_AS_NEEDED);
        jScrollPane1.setVerticalScrollBarPolicy(
        JScrollPane.VERTICAL_SCROLLBAR_AS_NEEDED); 
        try {
            Class.forName("com.mysql.cj.jdbc.Driver").newInstance();
            Connection con = DriverManager.getConnection("jdbc:mysql://127.0.0.1:3306/biblioteca", Login.usuario, Login.contra);
            PreparedStatement ps = con.prepareStatement("SELECT * FROM lector");
            ResultSet rs = ps.executeQuery();
            while(rs.next()){
                rfc = rs.getString("rfc");
                nombre = rs.getString("nombre");
                apellido = rs.getString("apellidos");
                genero = rs.getString("sexo");
                num = rs.getString("telefono");
                model.addRow(new Object[]{rfc,nombre,apellido,genero,num});
            }
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_formWindowOpened

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        DatabaseConnectivity db = new DatabaseConnectivity(Login.usuario,Login.contra);

        String rfc = jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString();
        String qry = "DELETE FROM lector WHERE rfc = '"+rfc+"'";
        boolean r = db.execute(qry);
        if (r) {
            formWindowOpened(null);
            Tools.Helper.show(this, "Lector Eliminado");
        } else {
            Tools.Helper.show(this, "Intentar de nuevo");
        }


    }//GEN-LAST:event_jButton2ActionPerformed

